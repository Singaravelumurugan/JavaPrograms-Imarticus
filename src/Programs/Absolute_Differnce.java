package Programs;

public class Absolute_Differnce {

		public static void main(String[] args) {
			System.out.println(Math.abs(4-10));
			System.out.println(Math.min(12,34));
			System.out.println(Math.max(55,87));
			System.out.println(Math.floor(10.534));
			System.out.println(Math.round(12.278));
			System.out.println(Math.ceil(12.378));
		}
	}


